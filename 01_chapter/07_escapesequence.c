// \t - Horizontal Tab: Moves the cursor to the next tab stop.
// \\ - Backslash: Prints a literal backslash (\).
// \' - Single Quote: Prints a single quote character (').
// \" - Double Quote: Prints a double quote character (").
#include<stdio.h>
int main(){
    printf("hey, my name is \n manish \n");
    printf("Virat Kohli is \tGOAT \n");
    printf("this is \\n move in a next line \n");
    printf("this is use for \"whdhew\" this");
    return 0;
}